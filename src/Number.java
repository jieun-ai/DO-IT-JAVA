
public class Number {

	public static void main(String[] args) {
		
		System.out.println(6+2);
		System.out.println(6-2);
		System.out.println(6*2);
		System.out.println(6/2);
		System.out.println(Math.PI);
		System.out.println(Math.floor(Math.PI)); // floor:내림
		System.out.println(Math.ceil(Math.PI)); // ceil: 올림

	}

}
